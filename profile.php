<div class="w3-row-padding">
  <div class="w3-third">
    <h2>Infrastructure-as-a-Service (IaaS)</h2>
    <p align="justify">IaaS means you're buying access to raw computing hardware over the Net, such as servers or storage. Since you buy what you need and pay-as-you-go, this is often referred to as utility computing. Ordinary web hosting is a simple example of IaaS: you pay a monthly subscription or a per-megabyte/gigabyte fee to have a hosting company serve up files for your website from their servers.</p>
  </div>
  
  <div class="w3-third">
    <h2>Platform-as-a-Service (PaaS) </h2>
    <p align="justify">PaaS means you develop applications using Web-based tools so they run on systems software and hardware provided by another company. So, for example, you might develop your own ecommerce website but have the whole thing, including the shopping cart, checkout, and payment mechanism running on a merchant's server. App Cloud (from salesforce.com) and the Google App Engine are examples of PaaS.</p> 
  </div>
  
  <div class="w3-third">
    <h2>Software-as-a-Service (SaaS )</h2>
    <p align="justify">SaaS is a software deployment model that delivers software applications as online, on-demand services. Software vendors increasingly deliver their software applications as online services, in accordance with the above-mentioned SaaS model, and thus become SaaS providers. This enables their customers to use these software services on demand.</p>
  </div>
  
